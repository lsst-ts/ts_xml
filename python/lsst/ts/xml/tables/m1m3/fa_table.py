def force_actuator_from_id(actuator_id: int) -> ForceActuatorData:
    """Returns ForceActuatorData with given actuator ID.

    Parameters
    ----------
    actuator_id : `int`
        Actuator ID - three digit number, with leading digit corresponding to
        the force actuator index.

    Returns
    -------
    force_actuator : ForceActuatorData
        Record (ForceActuatorData class) for actuator with given actuator ID.

    Raises
    ------
    RuntimeError
        When actuator with given ID cannot be found.
    """
    try:
        return next(fa for fa in FATable if fa.actuator_id == actuator_id)
    except StopIteration:
        raise RuntimeError(f"Cannot find force actuator with ID {actuator_id}")


def actuator_id_to_index(
    actuator_id: int, index_type: FAIndex = FAIndex.PRIMARY
) -> int | None:
    """Returns index for given actuator ID.

    Parameters
    ----------
    actuator_id : `int`
        Actuator ID - three digit number, with leading digit corresponding to
        the force actuator index.
    index_type : FAIndex
        Index type. See FAIndex enumeration for possible values.

    Returns
    -------
    index : `int`
        Index_type index value of the force actuator with actuator ID ==
        actuator_id.

    Raises
    ------
    RuntimeError
        When actuator with given ID cannot be found.
    """
    fa = force_actuator_from_id(actuator_id)
    return fa.get_index(index_type)
