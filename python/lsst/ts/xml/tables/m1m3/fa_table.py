"""This module main function is to provide FATable. FATable is a list of
ForceActuatorData, describing various physical and control-system related
characteristics of the M1M3 force actuators.

Attributes
----------
FATable : `[ForceActuatorData]`
"""


class FAType(enum.IntEnum):
    """Distinguish between single and dual axis actuators."""

    SAA = 0
    DAA = 1


class FASubnet(enum.IntEnum):
    """Control bus of the M1M3 Force Actuator."""

    A = 1
    B = 2
    C = 3
    D = 4


class FAOrientation(enum.IntEnum):
    """Orientation of the M1M3 force actuator."""

    NA = 0
    Y_PLUS = 1
    Y_MINUS = 2
    X_PLUS = 3
    X_MINUS = 4


class FAIndex(enum.IntEnum):
    """Enumeration to select index for various transformation functions."""

    X = 1
    Y = 2
    Z = 3
    PRIMARY = 4
    SECONDARY = 5


"""Number of M1M3 X lateral force actuators."""
FATABLE_XFA = 12

"""Number of M1M3 Y longitudinal force actuators."""
FATABLE_YFA = 100

"""Number of M1M3 Z force actuators."""
FATABLE_ZFA = 156

"""Number of M1M3 hardpoints."""
HP_COUNT = 6


@dataclass
class ForceActuatorData:
    """Represents a constant attributes for a single M1M3 force actuator. Those
    are vital for various visualization and calculations.

    X, Y and Z position are provided in meters from a reference origin, which
    lies at intersection of the optical and axis and elevation axis. When
    viewing mirror from above (M2) at horizon, +X is right and +Y is up - axis
    are oriented according to right hand rule. Z axis is pointing towards
    observer, and Z when zenith pointing. See LTS-213 for drawings.

    Attributes
    ----------
    index : `int`
        Force actuator 0-based index
    actuator_id : `int`
        Force actuator ID. Those are 3-digit numbers, where the first digit
        (hundreds) is force actuator quadrant, following two digits are its
        index in the quadrant.
    x_position : `int`
        X position in mirror cell.
    y_position : `int`
        Y position in mirror cell.
    z_position : `int`
        Z position in mirror cell.
    actuator_type : FAType
        Dual/single force actuator.
    subnet : FASubnet
        ILC subnet (1-4, A-D).
    address : `int`
        ILC Modbus address on subnet.
    orientation : FAOrientation
        X/Y force actuator orientation.
    x_index : `int`
        Force actuator X index. None if there isn't a cylinder in X direction.
    y_index : `int`
        Force actuator Y index. None if there isn't a cylinder in Y direction.
    z_index : `int`
        Force actuator Z index.
    s_index : `int`
        Force actuator secondary cylinder index. None for single axis FAs.
    near_neighbors : `[int]`
        Actuator ID of adjacent force actuators. This is primarily used in
        mirror safety checks.
    far_neighbors : `[int]`
        Actuator ID of force actuators adjacent to near neighbors. Used
        primarily to mirror safety checks, together with near_neighbors.
    """

    index: int
    actuator_id: int
    x_position: float
    y_position: float
    z_position: float
    actuator_type: FAType
    subnet: FASubnet
    address: int
    orientation: FAOrientation
    x_index: int | None
    y_index: int | None
    z_index: int
    s_index: int | None
    near_neighbors: list[int]
    far_neighbors: list[int]

    @property
    def quadrant(self) -> int:
        """Returns actuator quadrant.
        Returns
        -------
        quadrant : `int`
            Actuator quadrant (1-4).
        """
        return self.actuator_id // 100

    def get_index(self, index_type: FAIndex) -> int | None:
        """Returns given index.

        Parameters
        ----------
        index_type : FAIndex
            Returned index type. See FAIndex enum values.

        Returns
        -------
        index : `int`
            Force actuator index of given type. None if the force actuator
            isn't assigned an index.
        """
        if index_type == FAIndex.X:
            return self.x_index
        elif index_type == FAIndex.Y:
            return self.y_index
        elif index_type == FAIndex.Z:
            return self.z_index
        elif index_type == FAIndex.PRIMARY:
            return self.index
        elif index_type == FAIndex.SECONDARY:
            return self.s_index
        raise RuntimeError(f"Unknow index type {index_type}.")

    def near_neighbors_indices(
        self, index_type: FAIndex
    ) -> typing.Generator[int, None, None]:
        """Returns indices of near neighbors actuators.

        Parameters
        ----------
        index_type : FAIndex
            Index type of the returned indices. See FAIndex enum values.

        Returns
        -------
        indices : `[int]`
            Indices of near neighbors actuators.
        """
        for fa_id in self.near_neighbors:
            index = actuator_id_to_index(fa_id, index_type)
            if index is not None:
                yield index

    def far_neighbors_indices(
        self, index_type: FAIndex
    ) -> typing.Generator[int, None, None]:
        """Returns far neighbor indices.

        Parameters
        ----------
        index_type : FAIndex
            Index typ of the returned indices. See FAIndex enum values.

        Returns
        -------
        indices : `[int]`
            Indices of far neighbors. That includes both near and purely far
            neighbors.
        """
        for fa_id in self.far_neighbors:
            index = actuator_id_to_index(fa_id, index_type)
            if index is not None:
                yield index

    def only_far_neighbors_indices(
        self, index_type: FAIndex
    ) -> typing.Generator[int, None, None]:
        """Returns only far neighbor indices. Compared to far_neighbors_indices
        method, near neighbors are not returned.

        Parameters
        ----------
        index_type : FAIndex
            Index typ of the returned indices. See FAIndex enum values.

        Returns
        -------
        indices : `[int]`
            Indices of pure far neighbors. That excludes near neighbors.
        """
        for fa_id in self.far_neighbors:
            if fa_id not in self.near_neighbors:
                index = actuator_id_to_index(fa_id, index_type)
                if index is not None:
                    yield index

    def distance(self, another: typing.Self) -> float:
        """Returns distance between the actuators in the X and Y plane. All
        actuators shall be placed at the same Z plane, so Z is not considered
        in the calculation.

        Parameters
        ----------
        another : ForceActuatorData
            Force actuator to which distance will be measured.

        Returns
        distance : `float`
            Distance (in meters) between this and another force actuator.
        """
        return sqrt(
            (self.x_position - another.x_position) ** 2
            + (self.y_position - another.y_position) ** 2
        )


